C
      SUBROUTINE SUBCSQ(TWOCSQ,AR,AI,BR,BI,N,NC,NL,NS)
      IMPLICIT REAL*8 (A-H, O-Z)
C
C === THIS ROUTINE COMPUTES THE CONTRIBUTION TO THE COMMUTATOR [F,D] OF THE
C === TERM C**2 WHICH IS ADDED TO THE DIAGONAL SMALL COMPONENT ELEMENTS OF
C === THE FOCK-DIRAC MATRIX
C
      DIMENSION AR(N,N), AI(N,N)
      DIMENSION BR(*), BI(*)
C
      DO 100 I = NL + 1, NC 
      IJ = I * (I - 1) / 2
      DO 110 J = 1, NL
      IJ = IJ + 1
      BR(IJ) = BR(IJ) - TWOCSQ * AR(I,J)
      BI(IJ) = BI(IJ) - TWOCSQ * AI(I,J)
  110 CONTINUE
  100 CONTINUE
C
      RETURN
      END
