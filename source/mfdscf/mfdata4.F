C
C     ==================
      SUBROUTINE MFDATA4
C     ==================
      IMPLICIT REAL*8 (A-H, O-Z)
C
C ==============================================================
C
C INITIALIZE DATA,DEFINE PARAMETERS,STORE ALL COMMON BLOCKS USED
C FILES ARE OPENED AND CLOSED WHEN NEEDED
C
C FILES IN USE :
C NAME----UNIT------------PURPOSE-------------
C INPUT    5     INPUT DATA
C MFDSYMC 64     BINARY INPUT FROM MOLFDIR. SYMMETRY TRANSF. COEFF.
C MFDONE  62     BINARY INPUT FROM RELONEL(ONE ELECTRON INTEGRALS)
C MFDOEMR/I 60/70BINARY (DIRECT ACCESS) ONE-ELECTRON MATRICES
C SRTTWLL 40     BINARY INPUT FROM RELTWEL(L-L COMP. TWO EL. INT.)
C SRTTWSL 41     BINARY INPUT FROM RELTWEL(L-S COMP. TWO EL. INT.)
C SRTTWSS 42     BINARY INPUT FROM RELTWEL(S-S COMP. TWO EL. INT.)
C SRTLBLL 50     BINARY INPUT FROM RELTWEL(L-L COMP. LABELS)
C SRTLBSL 51     BINARY INPUT FROM RELTWEL(S-L COMP. LABELS)
C SRTLBSS 52     BINARY INPUT FROM RELTWEL(S-S COMP. LABELS)
C SRTBREI 43     BINARY INPUT FROM RELTWEL(BREIT (SL/SL) INTEGRALS)
C SRTBREL 53     BINARY INPUT FROM RELTWEL(BREIT (SL/SL) LABELS)
C MFDVECB 61     BINARY INPUT/OUTPUT SCF-VECTORS (OCCUPIED ONES)
C MFDVECA 67     FORMATTED OUTPUT SCF-VECTORS (ALL)
C MFDENSM 66     DIRECT ACCESS BINARY DENSITYMATRIX ELEMENTS (SYMMETRY)
C                    FOR A SPECIFIED NUMBER OF ITERATIONS
C SSFOCK  71     BINARY FOCKMATRIX CONTRIBUTION FROM SS AND SL INTEGRALS
C SELVECF 65
C DIISFIL 69     BINARY DIRECT ACCESS FILE USED BY THE DIIS ROUTINES
C
C ===================================================================
C
      INCLUDE 'paracom.inc'
      INCLUDE 'general.inc'
      INCLUDE 'memory.inc'
#if defined (PARALLEL)
      INCLUDE 'mpidef.inc'
#else
      PARAMETER (mpirnk=0)
#endif
C
#if defined (BIT64)
      IF (N22.LT.INT(SQRT(REAL(4*LENGTH)))+1) THEN
#else
      IF (N22.LT.INT(DSQRT(DBLE(4*LENGTH)))+1) THEN
#endif
         IF (mpirnk.EQ.0) CALL Q2JOB(3,'MFDATA4','N22',
#if defined (BIT64)
     &   INT(SQRT(REAL(4*LENGTH)))+1)
#else
     &   INT(DSQRT(DBLE(4*LENGTH)))+1)
#endif
      ENDIF
      RETURN
      END
