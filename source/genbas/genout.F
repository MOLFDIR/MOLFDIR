C     =================
      SUBROUTINE GENOUT
C     =================
      IMPLICIT REAL*8 (A-H, O-Z)
C
C     WRITE OUTPUT TO ENABLE USER TO CHOOSE THE SIGNIFICANT VECTORS
C
      INCLUDE 'paracom.inc'
C
      DIMENSION LTYP(-1:N4),LS(2)
      CHARACTER*2 LTYP*1,AST*18,VERSION*6,LS*5
      DATA VERSION /'1.4  '/
      DATA LTYP /' ','S','P','D','F','G','H'/
      DATA LS  /'LARGE','SMALL'/
      DATA AST /'******************'/
C
      WRITE (*,1000) AST,VERSION,DATES(2),TIMES(2),AST
      IF (.NOT.TWOC) WRITE (*,1010) NAMES (1)
      IF (TWOC) WRITE (*,1011) NAMES (1)
      IF (USENAT) THEN
         WRITE (6,*)
         WRITE (6,*) 'USING NATURAL ORBITALS'
         WRITE (6,*)
 	 WRITE (6,*) 'FILE IS: ',FILENAM
	 WRITE (6,*)
      ENDIF
      WRITE (*,1012) ((NUMCOF(IOM,IT),IT=0,N4),IOM=1,2)
      WRITE (*,1020) NSYMRP
      WRITE (*,1030) (IREPS(I),I=1,IRP)
      WRITE (*,1031)
      WRITE (*,1040)
      IF (IPRINT.GE.0) THEN
         DO 20 I = 1,IRP
	 IF (IPRTNR(IREPS(I)).EQ.1) THEN
           IR = IREPS (I)
           WRITE (*,1050) IR,(LTYP(ITPCOF(IR,J)),J=1,NLARGE(IR))
           WRITE (*,1051) (LTYP(ITPCOF(IR,J)),J=NLARGE(IR)+1,NTYPES(IR))
         ENDIF
   20    CONTINUE
         WRITE (*,1060)
         KMO = 1
         DO 30 I = 1,IRP
	 IF (IPRTNR(IREPS(I)).EQ.1) THEN
           WRITE (*,1070) IREPS (I)
           WRITE (*,1080) (LMO,ORBENE(LMO),LMO=KMO,
     &                     KMO+NBLR(I)-1)
	   KMO = KMO + NBLR(I)
	 ENDIF
   30    CONTINUE
      ENDIF
      RETURN
C     -------------
      ENTRY GENOUT2
C     -------------
C
C     WRITE RESULT OF CONTRACTION PROCES
C
      WRITE (*,1100)
      IF (IPRINT.GE.1) THEN
          DO 60 IOM = 1, 2
          DO 50 ITP = 0, N4
          IF (NCONTR(IOM,ITP).EQ.0) GOTO 50
          WRITE (*,1110) LS(IOM),LTYP(ITP)
          DO 40 I = 1, NCONTR (IOM,ITP)
          VPAR = VPART (I,ITP,IOM) * 100.D0
          IND = INDMO (I,ITP,IOM)
          IF (IND.GT.NMO) THEN
             INDN = MOS(IND,1)
             WRITE (*,1120) I,VPAR,INDN
          ELSEIF (IND.GT.0) THEN
             MO = MOS (IND,1)
             WRITE (*,1121) I,VPAR,MO,ORBENE(MO)
          ELSE
             IND = - IND
             JTP = IND / (N16 + 1) - 1
             J = MOD (IND,(N16+1))
             WRITE (*,1122) I,VPAR,LS(1),LTYP(JTP),J
          ENDIF
   40     CONTINUE
   50     CONTINUE
   60     CONTINUE
      ENDIF
      WRITE (*,1130) ((NCONTR (IOM,IT),IT=0,N4),IOM=1,2)
      WRITE (*,1140) NAMES(2)
 1000 FORMAT (/3X,A18/3X,'* PROGRAM GENBAS *',/3X,'* VERSION ',A6,' *',
     &/3X,'*',1X,A10,5X,'*',/3X,'*',2X,A8,6X,'*',/3X,A18)
 1010 FORMAT (//3X,'BASIS FROM CALCULATION : ',A)
 1011 FORMAT (//3X,'BASIS FROM 2-COMP. CALCULATION : ',A)
 1012 FORMAT (/3X,'NUMBER OF (CONTRACTED) GAUSSIANS :'//21X,
     &'S   P   D   F   G   H',/3X,
     &'LARGE COMPONENT',6I4,/3X,'SMALL COMPONENT',6I4)
 1020 FORMAT (/3X,'NUMBER OF REPRESENTATIONS',I3)
 1030 FORMAT (/3X,'OCCUPIED REPRESENTATIONS',16I3)
 1031 FORMAT (/3X,'FROM NOW ON ONLY THE SYMMETRY UNIQUE REPRESENTATIONS',
     &' ARE USED')
 1040 FORMAT (//3X,'DISTRIBUTION OF BASISFUNCTIONS OVER',
     &' THE REPRESENTATIONS'/)
 1050 FORMAT (3X,I3,' LARGE ',10(2X,A1))
 1051 FORMAT (6X,' SMALL ',10(2X,A1))
 1060 FORMAT (/3X,'CHARACTERISATION OF THE (LARGE COMPONENT) ORBITALS',
     & ' (# M.O. , ORBITALENERGY)')
 1070 FORMAT (/3X,'REPRESENTATION ',I3/)
 1080 FORMAT (4(1X,I4,1X,F14.7))
 1100 FORMAT (//3X,'GENERAL CONTRACTION COMPLETED')
 1110 FORMAT (/3X,A5,1X,A1,' BASIS WAS EXTRACTED FROM :')
 1120 FORMAT (I4,1X,G9.3,' % OF FUNCTION ',I3)
 1121 FORMAT (I4,1X,G9.3,' % OF VECTOR ',I4,' ( ORB. EN. ',F14.7,' )')
 1122 FORMAT (I4,1X,G9.3,' % OF KINETIC BALANCE ON ',A5,1X,A1,I3)
 1130 FORMAT (//3X,'NUMBER OF UNIQUE, GENERALLY CONTRACTED,',
     &' BASISFUNCTIONS :'//21X,'S   P   D   F   G   H',/3X,
     &'LARGE COMPONENT',6I4,/3X,'SMALL COMPONENT',6I4)
 1140 FORMAT (/3X,'BASIS WRITTEN TO FILE : ',A)
      END
